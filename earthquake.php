                <h2>Gorkha Earthquake 2015</h2>
                <p class="lead">What happened after the <strong>Gorkha Earthquake</strong>?</p>
    </div>
    <div class="row">
        <div class="col-md-4 wow fadeInDown" style="text-align:justify">
        <p>The April 2015 Nepal earthquake (Gorkha earthquake) killed more than 8,800 people and injured more than 23,000. It occurred at 11:56 (local time) on 25 April, with a magnitude of 7.8Mw or 8.1Ms and a maximum Mercalli Intensity of IX (Violent). Its epicenter was east of the district of Lamjung, and its hypocenter was at a depth of approximately 15 km (9.3 mi).It was the worst natural disaster to strike the country since the 1934 (1990 B.S) Nepal–Bihar earthquake</p>
        </div>

        <div class="col-md-4 wow fadeInDown " style="text-align:justify">
        <p>The earthquake triggered an avalanche on Mount Everest, killing at least 19, making it the deadliest day on the mountain in history. It triggered another huge avalanche in the Langtang valley, where 250 people were reported missing. Hundreds of thousands of people were made homeless with entire villages flattened, across many districts of the country. Centuries-old buildings were destroyed at UNESCO World Heritage sites in the Kathmandu Valley, including some at the Kathmandu Durbar Square, the Patan Durbar Squar, the Bhaktapur Durbar Square, the Changu Narayan Temple and the Swayambhunath Stupa. Geophysicists and other experts had warned for decades that Nepal was vulnerable to a deadly earthquake, particularly because of its geology, urbanization, and architecture.</p>
        </div>

        <div class="col-md-4 wow fadeInDown" style="text-align:justify">
        <p>Continued aftershocks occurred throughout Nepal within 15–20 minute intervals, with one shock reaching a magnitude of 6.7 on 26 April at 12:54:08 NST. The country also had a continued risk of landslides. A major aftershock occurred on 12 May 2015 at 12:51 NST with a moment magnitude (Mw) of 7.3. The epicenter was near the Chinese border between the capital of Kathmandu and Mt. Everest. More than 200 people were killed and more than 2,500 were injured by this aftershock.</p>
        </div>
